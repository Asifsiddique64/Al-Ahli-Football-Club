<h1>History</h1>
<p>Al-Ahli Saudi Football Club (Arabic: النادي الاهلي السعودي‎) is a Saudi Arabian professional football club based in Jeddah, that competes in the Saudi Pro League, the top flight of Saudi Arabian football. The club was founded in 1937.

Domestically, Al-Ahli have won 3 league titles, 13 King Cups, 6 Crown Prince Cups, 3 Prince Faisal bin Fahd Cups and 1 Super Cup. In international club football, Al-Ahli have won 3 GCC Champions League and 1 Arab Club Championship and have reached two AFC Champions League finals.

Al-Ahli is one of the four founding members of the Saudi Pro League that have never been relegated from the top flight, along with Al-Hilal, Al-Ittihad, and Al-Nassr. Al-Ahli have a record-breaking 51-match unbeaten run from 2014-2016.

Al Ahli's home games are played at King Abdullah Sports City, also known as the KASC Stadium. The stadium, which is shared with long-lasting city rivals Al-Ittihad, is the second largest stadium in Saudi Arabia, with a total capacity of 62,000.

The club's most famous Saudi players are Taisir Al-Jassim, Khalid Massad, Amin Dabo, Mohamed Abd Al-Jawad, Malek Mouath, and Yasser Al Mosailem. And the most famous foreign players are Omar Al Soma, Victor Simões, Nabil Maâloul, Imad Al Hosni and Mohamed Barakat.Former and famous Brazilian coach Telê Santana coached Al-Ahli from 1983 to 1985 and won 2 titles, one is league 1984 and the second is King Cup in 1983. Al Ahli has played eighteen King Cup finals, won thirteen and lost only five.

From 2014 to 2016, under the management of Swiss coach Christian Gross, the team won three titles with him. 2014–15 Saudi Crown Prince Cup, 2015–16 Saudi Professional League and 2016 King Cup. Al Ahli has won every major competition in which it has competed, with the exception of the AFC Champions League (in this competition they have lost two finals, in 1985 Asian Club Championship and 2012 AFC Champions League. They are also the first Saudi Club who played the Asian Final. Al Ahli is one of the big 4 clubs at Saudi Arabia along with Al Hilal , Al Nasser, and the local rivals Al Ittihad.</p>
